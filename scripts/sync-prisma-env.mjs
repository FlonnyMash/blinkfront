import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { config } from "dotenv";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const envLocalPath = path.join(root, ".env.local");
const envPath = path.join(root, ".env");

config({ path: envLocalPath });

const databaseUrl = process.env.DATABASE_URL?.trim();

if (!databaseUrl) {
  if (fs.existsSync(envPath)) {
    process.exit(0);
  }
  console.warn(
    "sync-prisma-env: DATABASE_URL not found in .env.local — skipping .env creation.",
  );
  process.exit(0);
}

const contents = `# Generated by scripts/sync-prisma-env.mjs — do not edit DATABASE_URL here.
# Update DATABASE_URL in .env.local, then run: npm run db:sync-env

DATABASE_URL=${JSON.stringify(databaseUrl)}
`;

fs.writeFileSync(envPath, contents, "utf8");
console.log("sync-prisma-env: wrote DATABASE_URL to .env for Prisma CLI");
